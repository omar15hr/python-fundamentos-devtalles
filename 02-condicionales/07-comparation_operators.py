
# Igualdad ==
print(5 == 5)  # True
print(5 == 3)  # False
print("hello" == "hello")  # True

# Operador de desigualdad !=
print(5 != 5)  # False
print(5 != 3)  # True
print("hello" != "world")  # True

# Operador mayor que >
print(7 > 5)  # True
print(5 > 9)  # False

# Operador menor que <
print(7 < 5)  # False
print(5 < 9)  # True

# Operador mayor o igual que >=
print(7 >= 5)  # True
print(5 >= 9)  # False
print(5 >= 5)  # True

# Operador menor o igual que <=
print(7 <= 5)  # False
print(5 <= 9)  # True
print(5 <= 5)  # True